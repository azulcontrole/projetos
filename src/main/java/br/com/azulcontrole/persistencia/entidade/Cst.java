package br.com.azulcontrole.persistencia.entidade;

public class Cst {
		private Integer idCst;
		private String  codCst;
		private String  descricaoCst;
		
		public Integer getIdCst() {
			return idCst;
		}
		public void setIdCst(Integer idCst) {
			this.idCst = idCst;
		}
		public String getCodCst() {
			return codCst;
		}
		public void setCodCst(String codCst) {
			this.codCst = codCst;
		}
		public String getDescricaoCst() {
			return descricaoCst;
		}
		public void setDescricaoCst(String descricaoCst) {
			this.descricaoCst = descricaoCst;
		}
		
		
}
