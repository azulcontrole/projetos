package br.com.azulcontrole.persistencia.entidade;

public class AgenciaBancaria {
			 private Integer id;
			 private Integer idPessoa;
			 private String nomeBanco;
			 private String agenciaBanco;
			 private String contaBanco;
			 private String gerenteBanco;
			 private Integer idBancos;
			public Integer getId() {
				return id;
			}
			public void setId(Integer id) {
				this.id = id;
			}
			public Integer getIdPessoa() {
				return idPessoa;
			}
			public void setIdPessoa(Integer idPessoa) {
				this.idPessoa = idPessoa;
			}
			public String getNomeBanco() {
				return nomeBanco;
			}
			public void setNomeBanco(String nomeBanco) {
				this.nomeBanco = nomeBanco;
			}
			public String getAgenciaBanco() {
				return agenciaBanco;
			}
			public void setAgenciaBanco(String agenciaBanco) {
				this.agenciaBanco = agenciaBanco;
			}
			public String getContaBanco() {
				return contaBanco;
			}
			public void setContaBanco(String contaBanco) {
				this.contaBanco = contaBanco;
			}
			public String getGerenteBanco() {
				return gerenteBanco;
			}
			public void setGerenteBanco(String gerenteBanco) {
				this.gerenteBanco = gerenteBanco;
			}
			public Integer getIdBancos() {
				return idBancos;
			}
			public void setIdBancos(Integer idBancos) {
				this.idBancos = idBancos;
			}

}
